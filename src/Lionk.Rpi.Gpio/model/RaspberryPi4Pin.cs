namespace Lionk.Rpi.Gpio;

/// <summary>
/// This enum represents the GPIO pin numbers on the Raspberry Pi 4, with physical pin numbers as comments.
/// </summary>
public enum RaspberryPi4Pin
{
    /// <summary>
    /// GPIO 2 (Physical Pin 3, SDA1, I2C).
    /// </summary>
    GPIO2 = 2,

    /// <summary>
    /// GPIO 3 (Physical Pin 5, SCL1, I2C).
    /// </summary>
    GPIO3 = 3,

    /// <summary>
    /// GPIO 4 (Physical Pin 7, GPCLK0).
    /// </summary>
    GPIO4 = 4,

    /// <summary>
    /// GPIO 5 (Physical Pin 29).
    /// </summary>
    GPIO5 = 5,

    /// <summary>
    /// GPIO 6 (Physical Pin 31).
    /// </summary>
    GPIO6 = 6,

    /// <summary>
    /// GPIO 7 (Physical Pin 26, SPI CE1).
    /// </summary>
    GPIO7 = 7,

    /// <summary>
    /// GPIO 8 (Physical Pin 24, SPI CE0).
    /// </summary>
    GPIO8 = 8,

    /// <summary>
    /// GPIO 9 (Physical Pin 21, SPI MISO).
    /// </summary>
    GPIO9 = 9,

    /// <summary>
    /// GPIO 10 (Physical Pin 19, SPI MOSI).
    /// </summary>
    GPIO10 = 10,

    /// <summary>
    /// GPIO 11 (Physical Pin 23, SPI SCLK).
    /// </summary>
    GPIO11 = 11,

    /// <summary>
    /// GPIO 12 (Physical Pin 32, PWM0).
    /// </summary>
    GPIO12 = 12,

    /// <summary>
    /// GPIO 13 (Physical Pin 33, PWM1).
    /// </summary>
    GPIO13 = 13,

    /// <summary>
    /// GPIO 14 (Physical Pin 8, TXD0).
    /// </summary>
    GPIO14 = 14,

    /// <summary>
    /// GPIO 15 (Physical Pin 10, RXD0).
    /// </summary>
    GPIO15 = 15,

    /// <summary>
    /// GPIO 16 (Physical Pin 36).
    /// </summary>
    GPIO16 = 16,

    /// <summary>
    /// GPIO 17 (Physical Pin 11).
    /// </summary>
    GPIO17 = 17,

    /// <summary>
    /// GPIO 18 (Physical Pin 12, PCM_CLK).
    /// </summary>
    GPIO18 = 18,

    /// <summary>
    /// GPIO 19 (Physical Pin 35, PCM_FS).
    /// </summary>
    GPIO19 = 19,

    /// <summary>
    /// GPIO 20 (Physical Pin 38).
    /// </summary>
    GPIO20 = 20,

    /// <summary>
    /// GPIO 21 (Physical Pin 40).
    /// </summary>
    GPIO21 = 21,

    /// <summary>
    /// GPIO 22 (Physical Pin 15).
    /// </summary>
    GPIO22 = 22,

    /// <summary>
    /// GPIO 23 (Physical Pin 16).
    /// </summary>
    GPIO23 = 23,

    /// <summary>
    /// GPIO 24 (Physical Pin 18).
    /// </summary>
    GPIO24 = 24,

    /// <summary>
    /// GPIO 25 (Physical Pin 22).
    /// </summary>
    GPIO25 = 25,

    /// <summary>
    /// GPIO 26 (Physical Pin 37).
    /// </summary>
    GPIO26 = 26,

    /// <summary>
    /// GPIO 27 (Physical Pin 13).
    /// </summary>
    GPIO27 = 27,
}
